SELECT 
FLAT_ALBUMS_ITEMS.VALUE:release_date_precision::string AS FLAT_ALBUMS_ITEMS_RELEASE_DATE_PRECISION,
FLAT_ALBUMS.VALUE:offset::string AS FLAT_ALBUMS_OFFSET,
FLAT_ALBUMS_ITEMS.VALUE:id::string AS FLAT_ALBUMS_ITEMS_ID,
FLAT_ALBUMS_ITEMS.VALUE:name::string AS FLAT_ALBUMS_ITEMS_NAME,
FLAT_ALBUMS_ITEMS_ARTISTS_EXTERNAL_URLS.VALUE:spotify::string AS FLAT_ALBUMS_ITEMS_ARTISTS_EXTERNAL_URLS_SPOTIFY,
FLAT_ALBUMS_ITEMS_ARTISTS.VALUE:uri::string AS FLAT_ALBUMS_ITEMS_ARTISTS_URI,
FLAT_ALBUMS_ITEMS.VALUE:type::string AS FLAT_ALBUMS_ITEMS_TYPE,
FLAT_ALBUMS_ITEMS_IMAGES.VALUE:url::string AS FLAT_ALBUMS_ITEMS_IMAGES_URL,
FLAT_ALBUMS_ITEMS.VALUE:available_markets::string AS FLAT_ALBUMS_ITEMS_AVAILABLE_MARKETS,
FLAT_ALBUMS_ITEMS.VALUE:uri::string AS FLAT_ALBUMS_ITEMS_URI,
FLAT_ALBUMS.VALUE:total::string AS FLAT_ALBUMS_TOTAL,
FLAT_ALBUMS.VALUE:limit::string AS FLAT_ALBUMS_LIMIT,
FLAT_ALBUMS.VALUE:previous::string AS FLAT_ALBUMS_PREVIOUS,
FLAT_ALBUMS_ITEMS_ARTISTS.VALUE:id::string AS FLAT_ALBUMS_ITEMS_ARTISTS_ID,
FLAT_ALBUMS_ITEMS.VALUE:album_type::string AS FLAT_ALBUMS_ITEMS_ALBUM_TYPE,
FLAT_ALBUMS.VALUE:href::string AS FLAT_ALBUMS_HREF,
FLAT_ALBUMS_ITEMS_IMAGES.VALUE:height::string AS FLAT_ALBUMS_ITEMS_IMAGES_HEIGHT,
FLAT_ALBUMS_ITEMS_IMAGES.VALUE:width::string AS FLAT_ALBUMS_ITEMS_IMAGES_WIDTH,
FLAT_ALBUMS_ITEMS.VALUE:href::string AS FLAT_ALBUMS_ITEMS_HREF,
FLAT_ALBUMS_ITEMS_ARTISTS.VALUE:type::string AS FLAT_ALBUMS_ITEMS_ARTISTS_TYPE,
FLAT_ALBUMS_ITEMS.VALUE:total_tracks::string AS FLAT_ALBUMS_ITEMS_TOTAL_TRACKS,
FLAT_ALBUMS_ITEMS.VALUE:release_date::string AS FLAT_ALBUMS_ITEMS_RELEASE_DATE,
FLAT_ALBUMS.VALUE:next::string AS FLAT_ALBUMS_NEXT,
FLAT_ALBUMS_ITEMS_ARTISTS.VALUE:href::string AS FLAT_ALBUMS_ITEMS_ARTISTS_HREF,
FLAT_ALBUMS_ITEMS_ARTISTS.VALUE:name::string AS FLAT_ALBUMS_ITEMS_ARTISTS_NAME,
FLAT_ALBUMS_ITEMS_EXTERNAL_URLS.VALUE:spotify::string AS FLAT_ALBUMS_ITEMS_EXTERNAL_URLS_SPOTIFY
FROM {{source('spotify', 'new_releases_json')}},
LATERAL FLATTEN(input => ALBUMS) FLAT_ALBUMS,
LATERAL FLATTEN(input => FLAT_ALBUMS.value:items) FLAT_ALBUMS_ITEMS,
LATERAL FLATTEN(input => FLAT_ALBUMS_ITEMS.value:artists) FLAT_ALBUMS_ITEMS_ARTISTS,
LATERAL FLATTEN(input => FLAT_ALBUMS_ITEMS_ARTISTS.value:external_urls) FLAT_ALBUMS_ITEMS_ARTISTS_EXTERNAL_URLS,
LATERAL FLATTEN(input => FLAT_ALBUMS_ITEMS.value:external_urls) FLAT_ALBUMS_ITEMS_EXTERNAL_URLS,
LATERAL FLATTEN(input => FLAT_ALBUMS_ITEMS.value:images) FLAT_ALBUMS_ITEMS_IMAGES